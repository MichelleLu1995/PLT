open Ast
open Parser

let tokenize = function
| LPAREN -> "LPAREN"
| RPAREN -> "RPAREN"
| LBRACE -> "LBRACE"
| RBRACE -> "RBRACE"
| LSQBRACE -> "LSQBRACE"
| RSQBRACE -> "RSQBRACE"
| SEMI -> "SEMI"
| COLON -> "COLON"
| COMMA -> "COMMA"
| BAR -> "BAR"
| PLUS -> "PLUS"
| MPLUS -> "MPLUS"
| MINUS -> "MINUS"
| MMINUS -> "MMINUS"
| TIMES -> "TIMES"
| MTIMES -> "MTIMES"
| DIVIDE -> "DIVIDE"
| MDIVIDE -> "MDIVIDE"
| ASSIGN -> "ASSIGN"
| EXPONENT -> "EXPONENT"
| EQ -> "EQ"
| NEQ -> "NEQ"
| MEQ -> "MEQ"
| PLUSEQ -> "PLUSEQ"
| LT -> "LT"
| LEQ -> "LEQ"
| GT -> "GT"
| GEQ -> "GEQ"
| AND -> "AND"
| OR -> "OR"
| NOT -> "NOT"
| IF -> "IF"
| ELSE -> "ELSE"
| ELIF -> "ELIF"
| FOR -> "FOR"
| WHILE -> "WHILE"
| RETURN -> "RETURN"
| INT -> "INT"
| BOOL -> "BOOL"
| STRING -> "STRING"
| FLOAT -> "FLOAT"
| VOID -> "VOID"
| MATRIX -> "MATRIX"
| ROW -> "ROW"
| COLUMN -> "COLUMN"
| FILE -> "FILE"
| TUPLE -> "TUPLE"
| PRINT -> "PRINT"
| MAIN -> "MAIN"
| DEF -> "DEF"
| IN -> "IN"
| BOOL_LIT(bool) -> "BOOL_LIT"
| FLOAT_LIT(float) -> "FLOAT_LIT"
| INT_LIT(int) -> "INT_LIT"
| ID(string) -> "ID"
| STRING_LIT(string) -> "STRING_LIT"
| EOF -> "EOF"

let _=
	let lexbuf = Lexing.from_channel stdin in
	let rec print_tokens = function
	| EOF -> " "
	| token ->
			print_endline (tokenize token);
			print_tokens (Scanner.token lexbuf) in print_tokens (Scanner.token lexbuf)
